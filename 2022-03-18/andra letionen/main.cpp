#include <iostream>
using namespace std;
int main()
{
    // cout är skärmen
    // << är utmatningsoperatorn
    // endl betyder ny rad
    cout << "Hello world!" << endl;
    string in;
    // cin är tangentbordet
    // >> är inmatningsoperatorn
    // in är en variabel
    // en variabel är till för att innehålla
    // olika värden.
    // En textsräng som användaren matat in eller ett tal
    // till exempel
    cin >> in;
    cout << in << endl;
    return 0;
}
